#[cfg(test)]
mod tests {
	use super::DECODE_TABLE;

	#[test]
	fn test_table_is_bijective() {
		let mut seen = [false; 256];
		for &value in DECODE_TABLE.iter() {
			assert!(!seen[value as usize], "Value {} appears multiple times", value);
			seen[value as usize] = true;
		}

		let mut seen_encode = [false; 256];
		for (i, &value) in DECODE_TABLE.iter().enumerate() {
			assert!(!seen_encode[i], "Value {} appears multiple times in encode table", i);
			seen_encode[i] = true;
			assert_eq!(
				i as u8,
				super::ENCODE_TABLE[value as usize],
				"Encode/Decode tables are not inverses"
			);
		}
	}
}
